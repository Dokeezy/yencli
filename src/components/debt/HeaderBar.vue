<template>
  <div>
    <div class="header-debt">
      <p>
        Total: {{computeAmount()}}€
      </p>
      <label>Montrer les dettes payées : </label><input type="checkbox" v-model="$store.state.includePaidOnes" />
    </div>
  </div>
</template>

<script>
export default {
  props: {
    debts: {
      type: Array
    }
  },
  methods: {
    computeAmount() {
      let debtSum = 0;
      if (this.$store.state.includePaidOnes === true) {
        this.debts.forEach((debt) => {
          debtSum += debt.amount;
        });
        return debtSum;
      } else {
        this.debts.filter(function(debt) {
          return debt.status === "pending";
        }).forEach((debt) => {
          debtSum += debt.amount;
        });
        return debtSum;
      }
    }
  }
}
</script>

<style>
  .header-debt {
    padding: 20px;
    font-size: 1.8rem;
    width: 30%;
    margin: auto;
    text-align: center;
  }
</style>
